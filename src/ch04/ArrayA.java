package ch04;

public class ArrayA{
    public static void main(String[] args) {
        int [] nilai = {82,12,41,38,19,26,9,48,20,55,8,32,3};
        
        System.out.print("Nilai Array = ");
        for (int a = 0; a < nilai.length; a++) {
            System.out.print(nilai[a] + ", ");
        }         
    }
}